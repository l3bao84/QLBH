    // Remove all on table(management product)
    public void removeAllOnTable(JTable jTable) {
        while(true) {
            DefaultTableModel model = (DefaultTableModel) jTable.getModel();
            int rowCount = model.getRowCount();
            if(rowCount == 0) {
                break;
            }else {
                try {
                    model.removeRow(0);
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }
    
    
    
