    // Get products information from database
    public void getProInforFromDB(int currentIndex) {
        ArrayList<SanPham> listSP = SanPham_dao.getInstance().selectAll();
        Map<Object, Integer> mapSP = new HashMap<Object, Integer>();
        int value = 0;
        for (SanPham sanPham : listSP) {
            mapSP.put(sanPham, value);
            value++;
        }
        Set<Object> set = mapSP.keySet();
        
        for (Object object : set) {
            if(mapSP.get(object) == currentIndex) {
                showDetails((SanPham) object);
            }
        } 
    }

    
