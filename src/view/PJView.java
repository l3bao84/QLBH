    // Update product
    public void updateProduct() {
        SanPham sp = getProduct();
        String masp = sp.getMasp();
        
        String tensp = this.jTF_ql_tensp.getText();
        String dvt = this.jTF_ql_dvt.getText();
        float dongia = Float.valueOf(this.jTF_ql_dongia.getText());
        int sl = Integer.valueOf(this.jTF_ql_sl.getText());
        String url = this.jTF_ql_link.getText();
        
        SanPham sanPham = new SanPham(masp, tensp, dvt, dongia, sl, url);
        this.qLSpham.del(sp);
        this.qLSpham.insert(sanPham);
        SanPham_dao.getInstance().update(sanPham);
        
        DefaultTableModel model = (DefaultTableModel) jTb_quanlysp.getModel();
        int rowCount = model.getRowCount();
        for(int i = 0; i < rowCount; i++) {
            String proCode = model.getValueAt(i, 0) + "";
            if(masp.equalsIgnoreCase(proCode)) {
                jTb_quanlysp.setValueAt(masp, i, 0);
                jTb_quanlysp.setValueAt(tensp, i, 1);
                jTb_quanlysp.setValueAt(dvt, i, 2);
                jTb_quanlysp.setValueAt(dongia + "00 VND", i, 3);
                jTb_quanlysp.setValueAt(sl, i, 4);
                jTb_quanlysp.setValueAt(url, i, 5);
            }
        }
        clearProData();
    }
    
    
    // Delete product
    public void delProduct() {
        DefaultTableModel model = (DefaultTableModel) jTb_quanlysp.getModel();
	int iRow = jTb_quanlysp.getSelectedRow();
	int choose = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xóa sản phẩm này chứ");
	if(choose == JOptionPane.YES_OPTION) {
            SanPham sanPham = getProduct();
            this.qLSpham.del(sanPham);
            SanPham_dao.getInstance().del(sanPham);
            model.removeRow(iRow);
	}
    }
    
    
    // Find product by name(management product and sale product)
    public void findProByName(JTextField jTextField, JTable jTable) {
        String proName = jTextField.getText() + "";
        SanPham sp = new SanPham(); sp.setTensp(proName);
        ArrayList<SanPham> listSP = SanPham_dao.getInstance().selectByNames(sp);
        
        if(listSP.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy sản phẩm có tên " + proName);
        }else {
            removeAllOnTable(jTable);
            if(jTable == this.jTb_dssp) {
                for (SanPham sanPham : listSP) {
                    adddProOnSaleTable(sanPham);
                }
            }else if(jTable == this.jTb_quanlysp) {
                for (SanPham sanPham : listSP) {
                    addProOnTable(sanPham);
                }
            }
        } 
    }
    
    
