    // Check exist customer
    public KhanhHang checkExistCustomer() {
        String makh = GeneratorID.randomAlphaNumeric(6);
        String tenkh = this.jTF_tenkh.getText();
        String diachi = this.jTF_diachi.getText();
        String sdt = this.jTF_sdt.getText();

        KhanhHang kh = new KhanhHang(makh, tenkh, diachi, sdt);
        KhanhHang khanhHang = null;
        
        if((KhanhHang_dao.getInstance().selectByName(tenkh)) == null) {
            KhanhHang_dao.getInstance().insert(kh);
        }else {
            khanhHang = KhanhHang_dao.getInstance().selectByName(tenkh);
        }
        return khanhHang;
    }
    
    
    // Complete purchase
    public String completePurchase() {
        KhanhHang kh = checkExistCustomer();
        
        String tennv = this.jTF_pc_tennv.getText();
        NhanVien nv = NhanVien_dao.getInstance().selectByName(tennv);
        
        SanPham sp = getSPFromList();
        
        
        int amount = Integer.valueOf(this.jTF_amount.getText());
        float total = Float.valueOf(this.jLb_pc_total.getText().substring(0,7).trim());
        
        String mahd = GeneratorID.randomAlphaNumeric(6);
        
        Date date = new Date();
        String time = (date.getYear() + 1900) + "-" + ("0" + (date.getMonth() + 1)) + "-" + date.getDate();
        
        HoaDon hd = new HoaDon(mahd, kh, nv, time);
        HoaDon_dao.getInstance().insert(hd);
        
        CTHoaDon cthd = new CTHoaDon(hd, sp, amount, total);
        CTHoaDon_dao.getInstance().insert(cthd);
        
        return mahd;
    }
    
    
